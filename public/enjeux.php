          <h2 class="titlePAge twoline">
            Recyclage et économie circulaire<br/>
            <span class="subtitle">Le bronze industriel</span>
          </h2>
          <p><strong><span style="color:#373737;">LE RECYCLAGE A TOUJOURS FAIT ET CONTINUE TOUJOURS DE FAIRE PARTIE INTÉGRANTE<br/> DU PROCESSUS DE PRODUCTION DES MÉTAUX.</span><strong></p>

          <p>A la différence des produits organiques, les métaux, grâce à leurs propriétés intrinsèques, sont des éléments recyclables à l’infini. A chaque fusion, les caractéristiques techniques des métaux recyclés sont entièrement restituées. Ainsi les produits issus des matières premières secondaires sont parfaitement comparables à ceux qui proviennent des minerais.</p>
           
          <p>Aujourd’hui, les demi-produits métalliques (tubes, fils, plaques laminées) sont fabriqués à partir de matières premières primaires issues de minerais, comme de matières premières secondaires issues des sous-produits de l’industrie ou de produits à recycler en fin de vie.<br/> 
          La prédisposition des métaux  au recyclage permet d’afficher des taux de recyclage très élevés.</p>

          <ul>
          <li><strong>100% du plomb produit en France</strong> sont issus de matières premières secondaires,</li>
          <li><strong>44% du zinc de production</strong> et de première transformation sont issus du recyclage,</li>
          <li><strong>Plus de 50% du cuivre</strong>, du nickel, du fer, de l’aluminium, de l’acier ou encore de l’or<br/>
             sont déjà recyclés.</li>
          </ul>

          <p>L’exploitation des « mines urbaines », c’est-à-dire des produits et déchets en fin de vie d’une Europe mature au plan de la consommation de produits contenant des métaux, offre plusieurs avantages :</p>

          <ul>
          <li>Une valorisation plus durable des ressources naturelles,</li>
          <li>un avantage énergétique et donc une économie de carbone dans un contexte de rareté<br/>
             et de coût élevé de l’énergie, le recyclage des métaux offrant une efficacité énergétique<br/> 
             de deux à dix fois supérieure à celle de la fusion des métaux à partir de minerais vierges,</li>
          <li>un accroissement du degré de sécurité d’approvisionnement des utilisateurs aval.</li>
          </ul>
            
          <p><strong>Avec ses adhérents, la FEDEM est engagée</strong> pour permettre aux acteurs français et européens d’exploiter et valoriser ces mines urbaines au profit des industries utilisatrices aval. Ainsi, au-delà des entreprises présentes depuis longtemps dans le recyclage des métaux de base, celui-ci faisant partie intégrante du processus industriel du secteur, certaines entreprises adoptent depuis quelques années des stratégies de diversification intégrant le recyclage de métaux rares <strong>en se positionnant comme leader sur ces marchés</strong> (piles et accumulateurs, batteries, catalyseurs, déchets d’équipement électriques et électroniques…).</p>
           
          <p>Avec une demande en métaux augmentant de manière générale, le recyclage ne pourra pas compenser à lui seul les nouveaux besoins, mais il contribue à une industrie des matières premières plus durable, innovante et responsable. Pour cela, le recyclage doit bénéficier d’un fort et rapide encouragement afin que l’industrie des matières premières puisse tirer parti des immenses mines urbaines valorisables.</p>
